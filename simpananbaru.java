    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf= new SimpleDateFormat("yyyy-MM-dd");
        
        datacombobox dt2=(datacombobox) jComboBox1.getSelectedItem();
        String Anggota=(dt2.id);
        
        String NamaSimpanan=(jTextField3.getText());
        String BesarSimpanan=(jTextField2.getText().toString());
        String TanggalSimpan=sdf.format(jXDatePicker1.getDate());
        String Keterangan=(jTextArea1.getText());
        
        try {
            Statement statement=(Statement)
            koneksi.getConnection().createStatement();
            //executequery untuk menampilkan data
            //executeupdate untuk menambah/simpan, edit data
            String sql = "insert into tb_simpanan (nama_simpanan, id_anggota, tgl_simpanan, besar_simpanan, keterangan_simpanan) "
                    + "value('"+NamaSimpanan+"','"+Anggota+"','"+TanggalSimpan+"','"+BesarSimpanan+"','"+Keterangan+"')";
               
               statement.executeUpdate(sql);
               
               statement.close();
               JOptionPane.showMessageDialog(this, "Simpanan "+NamaSimpanan+" Disetor");
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Gagal \n"+e.getMessage());

        }
    }//GEN-LAST:event_jButton1ActionPerformed

