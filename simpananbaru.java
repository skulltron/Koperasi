    public void fillcomboanggota() {
            try {
                Statement statement=(Statement)
                    koneksi.getConnection().createStatement();
                String sql ="SELECT * FROM tb_anggota where status_anggota != 'Blacklist'";
            ResultSet r = statement.executeQuery(sql); 
            
               
            while(r.next()){
               jComboBox1.addItem(new datacombobox(r.getString("id_anggota"),
                r.getString("nama_anggota")));
            }
             
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
    }

